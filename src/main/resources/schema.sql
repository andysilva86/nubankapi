-- CRIA TABELAS PARA ARMAZENAR EM MEMORIA AS BASES PARA TESTES FEATURE
DROP TABLE IF EXISTS TBL_LOGIN;
CREATE TABLE TBL_LOGIN(
  ID_USUARIO INT AUTO_INCREMENT  PRIMARY KEY,
  USUARIO VARCHAR(250) NOT NULL,
  SENHA VARCHAR(6) NOT NULL,
  STATUS_AUTENTICACAO BOOLEAN
);

DROP TABLE IF EXISTS TBL_CLIENTE;
CREATE TABLE TBL_CLIENTE(
  ID_CONTA INT AUTO_INCREMENT  PRIMARY KEY,
  NOME VARCHAR(250) NOT NULL,
  SENHA VARCHAR(6) NOT NULL,
  SALDO REAL NOT NULL,
  CREDITOFACIL BOOLEAN,
  VALOR REAL DEFAULT 0,
  QTDMES INT DEFAULT 0
);

DROP TABLE IF EXISTS TBL_TRANSACAO;
CREATE TABLE TBL_TRANSACAO(
  ID_TRANSACAO INT AUTO_INCREMENT  PRIMARY KEY,
  ID_CONTA INT,
  DESCRICAO_T VARCHAR(250),
  VALOR_T REAL DEFAULT 0,
  DATA_T DATE
);

DROP TABLE IF EXISTS TBL_TRANSACAOFUTURA;
CREATE TABLE TBL_TRANSACAOFUTURA(
  ID_TRANSACAO INT AUTO_INCREMENT  PRIMARY KEY,
  ID_CONTA INT,
  DESCRICAO_T VARCHAR(250),
  VALOR_T REAL DEFAULT 0,
  DATA_T DATE
);